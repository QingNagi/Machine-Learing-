import scipy.constants as cn

print(cn.golden)    # 黄金比例
print(cn.c)   # 光速
print(cn.R)     # 摩尔气体常数
print(cn.alpha)   # 精细结构常数
print(cn.k)     # 玻尔兹曼常数
print(cn.degree)            # 1度角对应弧度
print(cn.degree * 180 / cn.pi)   # 求出度数
print(cn.minute)    # 一分钟的秒数
print(cn.hour)    # 一小时的秒数
print(cn.Julian_year)    # 365.25day
print(cn.inch)    # inch的米数
print(cn.mile)   # 英里
print(cn.light_year)
